a=int(input("1st number "))
c=int(input("2nd number "))
b=input("operator ")
if b=="+":
    print("addition",a+c)
if b=='-':
    print("subtraction",a-c)
if b=='*':
    print("multiplication",a*c)
if b=='/':
    print("division",a/c)
if b=='%':
    print("modulus",a%c)
if b=='//':
    print("floor division",a//c)
if b=='**':
    print("exponantion",a**c)



